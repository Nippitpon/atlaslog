// ETL: ExerciseDB dataset (hasaneyldrm/exercises-dataset) → Supabase seed SQL
//
// Usage:
//   node scripts/build-exercises.mjs <path-to-exercises.json>
//   (default input: ../exercises-dataset/data/exercises.json relative to this script)
//
// Reads the 1,324-exercise dataset and writes supabase/seed/exercises.seed.sql
// (batched INSERTs) for the `public.exercises` table. Media (GIFs) is NOT bundled;
// we store each record's media_id as gif_path and build the CDN URL client-side
// via exerciseGifUrl(). Only English instruction_steps are kept.
//
// Group mapping (dataset body_part → Atlaslog MUSCLE_GROUPS + new 'Other'):
//   waist→Core · upper/lower legs→Legs · back→Back · chest→Chest
//   upper/lower arms→Arms · shoulders→Shoulders · cardio/neck/other→Other

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const INPUT = process.argv[2]
  ?? resolve(__dirname, '../exercises-dataset/data/exercises.json')
const OUTPUT = resolve(__dirname, '../supabase/seed/exercises.seed.sql')
const BATCH = 200

const GROUP_MAP = {
  waist: 'Core',
  'upper legs': 'Legs',
  'lower legs': 'Legs',
  back: 'Back',
  chest: 'Chest',
  'upper arms': 'Arms',
  'lower arms': 'Arms',
  shoulders: 'Shoulders',
  cardio: 'Other',
  neck: 'Other',
}

function mapGroup(bodyPart) {
  return GROUP_MAP[String(bodyPart || '').toLowerCase()] ?? 'Other'
}

function titleCase(s) {
  return String(s || '')
    .split(/\s+/)
    .filter(Boolean)
    .map(w => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ')
}

// SQL literal helpers
function q(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}
function qOrNull(s) {
  return s == null || s === '' ? 'NULL' : q(s)
}
function textArray(arr) {
  if (!Array.isArray(arr) || arr.length === 0) return "ARRAY[]::text[]"
  return `ARRAY[${arr.map(q).join(',')}]::text[]`
}

const raw = JSON.parse(readFileSync(INPUT, 'utf8'))
const arr = Array.isArray(raw) ? raw : (raw.exercises ?? Object.values(raw))
console.log(`Read ${arr.length} exercises from ${INPUT}`)

const rows = arr.map(x => {
  const steps = x.instruction_steps?.en
    ?? (typeof x.instructions?.en === 'string' ? [x.instructions.en] : [])
  return {
    id: `db-${x.id}`,
    name: x.name ?? '',
    muscle_group: mapGroup(x.body_part),
    equipment: titleCase(x.equipment),
    target: x.target ?? null,
    secondary_muscles: Array.isArray(x.secondary_muscles) ? x.secondary_muscles : [],
    instructions: Array.isArray(steps) ? steps : [],
    gif_path: x.media_id ?? null,
  }
})

const cols = '(id, name, muscle_group, equipment, target, secondary_muscles, instructions, gif_path)'
const lines = []
lines.push('-- Auto-generated by scripts/build-exercises.mjs — do not edit by hand.')
lines.push('-- Source: hasaneyldrm/exercises-dataset (ExerciseDB v1). Media via CDN, non-commercial.')
lines.push(`-- ${rows.length} exercises. Run AFTER creating the table (SUPABASE_SETUP.md §2j).`)
lines.push('')

for (let i = 0; i < rows.length; i += BATCH) {
  const chunk = rows.slice(i, i + BATCH)
  lines.push(`insert into public.exercises ${cols} values`)
  const values = chunk.map(r =>
    `  (${q(r.id)}, ${q(r.name)}, ${q(r.muscle_group)}, ${qOrNull(r.equipment)}, ` +
    `${qOrNull(r.target)}, ${textArray(r.secondary_muscles)}, ${textArray(r.instructions)}, ${qOrNull(r.gif_path)})`
  )
  lines.push(values.join(',\n') + '\non conflict (id) do nothing;')
  lines.push('')
}

mkdirSync(dirname(OUTPUT), { recursive: true })
writeFileSync(OUTPUT, lines.join('\n'), 'utf8')

// Summary
const byGroup = {}
for (const r of rows) byGroup[r.muscle_group] = (byGroup[r.muscle_group] || 0) + 1
const noGif = rows.filter(r => !r.gif_path).length
console.log(`Wrote ${rows.length} rows → ${OUTPUT}`)
console.log('By group:', byGroup)
console.log(`Rows without gif_path: ${noGif}`)
